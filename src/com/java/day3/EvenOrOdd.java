package com.java.day3;

public class EvenOrOdd {
	
	public static void main(String[] args) {
		// divide by 2 remainder -> 0 --> even
		//java --> reaminder -> %
		
		// 5 % 2 --> 1
		// 10 % 2 --> 0
		int n=10; // if -else 
		
		if(n%2==0)
		{
			//the number is even
			System.out.println("Even ");
		}
		else
		{
			//odd
			System.out.println("Odd");
		}
		
	}

}
