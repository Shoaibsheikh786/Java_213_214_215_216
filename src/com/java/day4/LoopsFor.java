package com.java.day4;

public class LoopsFor {

	public static void main(String[] args) {
      
		
		
		// 3times 
		
		/* for(initlization;condition;inc/dec)
		 * {
		 *    // code which we want to repeat
		 *    
		 * }*/
		
		for(int i=0;i<3;i++)
		{
			System.out.println("Shoaib");
		}
		
		System.out.println("------------------------");
		
		
		for(int i=1;i>3;i++)
		{
			System.out.println("Hello World");
		}
		
       System.out.println("------------------------");	
		for(int i=0;i<3;i++)
		{  
			i++;  // 1
			System.out.println("Hello World Java");
			i++;  // 2
		}
		
		System.out.println("------------------------");	
		for(int i=0;i<3;i--)
		{  
			
			System.out.println("Hello World Java 2");
			
		}
		
	
		
		
	
		
	}

}
